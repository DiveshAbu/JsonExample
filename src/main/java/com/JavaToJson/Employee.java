package com.JavaToJson;

public class Employee {

	public int id;
	public String name;
	public float price;

	public int getid() {

		return id;
	}

	public void setid(int id) {

		this.id = id;
	}

	public String getname() {

		return name;

	}

	public void setname(String name) {

		this.name = name;
	}

	public float getprice() {

		return price;

	}

	public void setprice(float price) {

		this.price = price;
	}

}
